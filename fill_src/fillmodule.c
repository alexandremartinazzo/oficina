#include <pygobject.h>

void fill_register_classes (PyObject *d);

extern PyMethodDef fill_functions[];

DL_EXPORT(void)
initfill(void)
{
    PyObject *m, *d;
	
    init_pygobject ();

    m = Py_InitModule ("fill", fill_functions);
    d = PyModule_GetDict (m);
	
    fill_register_classes (d);

    if (PyErr_Occurred ()) {
	Py_FatalError ("can't initialise module fill");
    }

}
